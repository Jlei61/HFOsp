"""SNN spontaneous-event scan — does a HOMOGENEOUS spiking sheet, driven by SLOW
SPATIALLY-CORRELATED noise, produce robust discrete self-terminating population
events anywhere on the (drive x noise) plane, or only the rate-field's
silent<->sustained topology with NO robust discrete band?

Faithful mirror of the rate-field (drive x sigma) joint NULL
(docs/.../step1_noise_contract §11). Three faithfulness requirements (advisor 2026-06-05):

 1. NOISE = slow (tau~150 ms) AND spatially correlated (ell~l_EE) OU field added to
    the E external rate, generated by the SAME src.sef_hfo_events.make_ou_noise used on
    the rate field. The stock engine OU (model.py / kick_probe.py) is a SCALAR/global
    field -> can only brighten the whole sheet together -> cannot nucleate a LOCAL
    event -> a NULL there would be "global noise can't seed local events", NOT
    "homogeneous tissue can't support discrete events". So we add the spatial field.

 2. OBSERVABLE = COHERENCE active fraction: per time bin, local E-active fraction per
    grid cell, spatially smoothed (ell~l_EE), then fraction of cells above a fixed
    structural crossing. A blind distinct-neuron SUM is contiguity-blind (100 scattered
    == 100 contiguous) and inflates with speckle.

 3. DETECTOR = REUSE src.sef_hfo_events.classify_run / calibrate_detector / accepted_cell
    UNCHANGED (same discreteness contract: all-returned; frac_time_on>=0.30 -> sustained;
    per-op amplitude bar = max(sigma=0, sigma_ref) floor <-> sigma=0 kick peak midpoint;
    accept rate in [0.01,1]/s). floor uses BOTH sigma=0 and a sub-threshold sigma_ref run.

Verdict is TOPOLOGICAL: a robust 2-D discrete region ANYWHERE in (drive x sigma_n) ->
"SNN shows a spontaneous discrete band" (would DIFFER from the rate-field NULL); only a
seam / single cell / silent<->sustained -> "consistent with the rate-field NULL".
sigma_n (1/ms on the Poisson rate) is INCOMMENSURABLE with the rate-field sigma (mV on
muE) -- never read the verdict "at the rate field's sigma".

Run:  PYTHONPATH=<engine>:<repo> python noise_spontaneous_scan.py --mode controls
"""
