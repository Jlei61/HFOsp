# ---- fixed structural constants (NOT tuned to yield events) ----
GRID_N = 24            # noise / coherence grid over the LxL sheet
ELL_NOISE = 0.40       # mm  spatial correlation length of the slow noise (~ l_EE)
TAU_NOISE = 150.0      # ms  slow afferent component (matches stock engine tau_n)
ELL_COH = 0.38         # mm  coherence smoothing length (= l_EE)
COH_CROSS = 0.04       # local E-active fraction above which a cell is "coherently active"
BIN_MS = 2.0           # ms  coherence active-fraction sampling bin
# kick reference (same geometry as kick_probe.py)
R_KICK = 0.15          # mm
T_KICK = 150.0         # ms
DUR_KICK = 18.0        # ms


def _cell_index(pos, L, n):
    c = np.clip((pos / L * n).astype(int), 0, n - 1)
    return c[:, 0], c[:, 1]            # rows, cols


def simulate_field(p, net, sigma_noise, do_kick, noise_seed=0, tau_noise=TAU_NOISE,
                   verbose=False):
    """model.simulate's loop, with (a) a slow spatially-correlated OU field added to
    the E external rate (replacing the scalar global OU), (b) optional localized kick,
    and (c) a recorded COHERENCE active-fraction series ext_coh (per BIN_MS bin).

    Returns dict(ext_coh, dt_bin_ms, rate_E_hz, wall_s).
    """
    rng = net["rng"]
    NE, NI = net["NE"], net["NI"]
    N = NE + NI
    labels = net["labels"]; pos = net["pos"]
    ampa = net["ampa_by_delay"]; gaba = net["gaba_by_delay"]
    M = net["max_delay_steps"] + 1
    dt = p.dt
    nsteps = int(round(p.T / dt))

    decay_sE = np.exp(-dt / p.tau_r_AMPA); decay_IE = np.exp(-dt / p.tau_d_AMPA)
    decay_sI = np.exp(-dt / p.tau_r_GABA); decay_II = np.exp(-dt / p.tau_d_GABA)
    tau_m = np.where(labels == 0, p.tau_m_E, p.tau_m_I).astype(np.float64)
    decay_V = np.exp(-dt / tau_m)
    ref_steps = np.where(labels == 0, int(round(p.tau_ref_E / dt)),
                         int(round(p.tau_ref_I / dt))).astype(np.int32)
    ext_incr = (tau_m / p.tau_r_AMPA) * np.where(labels == 0, p.J_ext_E, p.J_ext_I)
    ampa_bins = [d for d in range(M) if ampa[d].nnz > 0]
    gaba_bins = [d for d in range(M) if gaba[d].nnz > 0]

    nu_theta, _, _ = compute_nu_theta(p)
    nu_sig = p.nu_ext_ratio * nu_theta

    # ---- slow spatially-correlated OU noise field (E external rate) ----
    is_E = labels == 0
    rows, cols = _cell_index(pos, p.L, GRID_N)
    stim_fn = make_ou_noise(GRID_N, p.L, dt, sigma_noise, tau_noise=tau_noise,
                            ell_noise=ELL_NOISE, seed=noise_seed)

    # ---- coherence observable: precompute per-cell E counts + smoothing kernel ----
    erow, ecol = rows[is_E], cols[is_E]
    flat_E = erow * GRID_N + ecol
    En_per_cell = np.bincount(flat_E, minlength=GRID_N * GRID_N).reshape(GRID_N, GRID_N)
    En_safe = np.maximum(En_per_cell, 1)
    K_coh = isotropic_gaussian(GRID_N, p.L, ELL_COH)

    # ---- kick mask ----
    center = np.array([p.L / 2, p.L / 2])
    dist_c = np.linalg.norm(pos - center, axis=1)
    kick_mask = is_E & (dist_c <= R_KICK)
    kick_boost = 2.0 * nu_theta

    # ---- state ----
    V = np.full(N, p.V_reset); ref = np.zeros(N, dtype=np.int32)
    s_E = np.zeros(N); I_E = np.zeros(N); s_I = np.zeros(N); I_I = np.zeros(N)
    ring_sE = np.zeros((M, N)); ring_sI = np.zeros((M, N))

    bin_steps = int(round(BIN_MS / dt))
    n_bins = nsteps // bin_steps
    ext_coh = np.zeros(n_bins)
    rate_E = np.zeros(nsteps)
    bin_active = np.zeros(NE, dtype=bool)      # OR of E spikes within the current bin

    t0 = time.time()
    for t in range(nsteps):
        tm = t * dt
        field = stim_fn(tm)                     # (GRID_N, GRID_N) slow spatial OU, or 0.0 if sigma=0
        nu_vec = np.full(N, max(nu_sig, 0.0))
        if np.ndim(field):
            nu_vec[is_E] += field[erow, ecol]   # E external rate += local slow noise
        if do_kick and (T_KICK <= tm < T_KICK + DUR_KICK):
            nu_vec[kick_mask] += kick_boost
        np.maximum(nu_vec, 0.0, out=nu_vec)

        s_E *= decay_sE; s_I *= decay_sI
        slot = t % M
        s_E += ring_sE[slot]; ring_sE[slot] = 0.0
        s_I += ring_sI[slot]; ring_sI[slot] = 0.0
        ext = rng.poisson(nu_vec * dt, size=N).astype(np.float64)
        s_E += ext * ext_incr
        I_E = s_E + (I_E - s_E) * decay_IE
        I_I = s_I + (I_I - s_I) * decay_II
        I_net = I_E - I_I

        ref -= 1; np.maximum(ref, 0, out=ref)
        free = ref == 0
        Vtmp = I_net + (V - I_net) * decay_V
        V = np.where(free, Vtmp, p.V_reset)
        spk = free & (V >= p.V_th)
        V[spk] = p.V_reset; ref[spk] = ref_steps[spk]

        rate_E[t] = spk[:NE].sum()
        bin_active |= spk[:NE]
        if spk.any():
            spE = np.where(spk[:NE])[0]; spI = np.where(spk[NE:])[0]
            if spE.size:
                for d in ampa_bins:
                    ring_sE[(t + d) % M] += np.asarray(ampa[d][:, spE].sum(axis=1)).ravel()
            if spI.size:
                for d in gaba_bins:
                    ring_sI[(t + d) % M] += np.asarray(gaba[d][:, spI].sum(axis=1)).ravel()

        # flush coherence bin
        if (t + 1) % bin_steps == 0:
            b = (t + 1) // bin_steps - 1
            if b < n_bins:
                act = np.bincount(flat_E[bin_active], minlength=GRID_N * GRID_N
                                  ).reshape(GRID_N, GRID_N)
                local_frac = act / En_safe
                sm = convolve_periodic(local_frac, K_coh)
                ext_coh[b] = float((sm > COH_CROSS).mean())
                bin_active[:] = False

        if verbose and (t % max(1, nsteps // 5) == 0):
            print(f"    sim {t}/{nsteps} ({tm:.0f}ms) rE={rate_E[t]/NE/dt*1e3:.1f}Hz "
                  f"coh={ext_coh[max(0,(t//bin_steps)-1)]:.3f} "
                  f"elapsed {time.time()-t0:.0f}s", flush=True)

    return dict(ext_coh=ext_coh, dt_bin_ms=BIN_MS, rate_E_hz=rate_E / NE / dt * 1e3,
                wall_s=time.time() - t0)


def fresh(p, net, seed):
    net["rng"] = np.random.default_rng(seed)
    return net


