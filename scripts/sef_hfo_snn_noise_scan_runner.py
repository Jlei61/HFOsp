def run_controls(L, density, T, drive=0.6):
    """Noise LADDER at one drive: sigma=0 (extinction + floor), sigma=0+kick (event
    peak), then a ladder of no-kick noise fractions. One shot shows: ignition onset,
    whether the coherence observable saturates, and discrete-vs-sustained across the
    noise axis. Also tells us the right floor (sub-threshold speckle level)."""
    base = dict(g=3.6, L=L, density=density, T=T, seed=1)
    p = Params(nu_ext_ratio=drive, **base)
    nu_theta = compute_nu_theta(p)[0]
    nu_sig = drive * nu_theta
    fracs = [0.02, 0.05, 0.10, 0.20, 0.40]      # noise std as fraction of nu_sig
    print(f"[controls] L={L} density={density} T={T} drive={drive}  "
          f"nu_sig={nu_sig*1e3:.0f}Hz  ladder(frac*nu_sig)={fracs}", flush=True)
    print("[build] ...", flush=True)
    net = build_network(p, verbose=False)

    r_sig0 = simulate_field(p, fresh(p, net, 1), 0.0, do_kick=False)
    r_kick = simulate_field(p, fresh(p, net, 1), 0.0, do_kick=True)
    floor0 = float(r_sig0["ext_coh"].max())
    peak = float(r_kick["ext_coh"].max())
    print(f"[controls] sigma=0 floor(max_coh)={floor0:.4f}  kick peak(max_coh)={peak:.4f}",
          flush=True)
    try:
        bar = calibrate_detector([r_sig0["ext_coh"]], r_kick["ext_coh"])["event_on_frac"]
    except UndetectableOperatingPoint as e:
        bar = None
        print(f"[controls] UNDETECTABLE (sigma=0 floor not below kick peak): {e}", flush=True)

    def show(tag, r):
        e = r["ext_coh"]
        line = (f"  {tag:16s} max_coh={e.max():.3f} mean={e.mean():.4f} "
                f"frac>0={float((e>0).mean()):.3f} rE_max={r['rate_E_hz'].max():.0f}Hz "
                f"wall={r['wall_s']:.0f}s")
        if bar is not None:
            c = classify_run(e, r["dt_bin_ms"], op={}, window="A", event_on_frac=bar)
            line += (f"  -> {c['label']}/{c['reason']} n_ev={c['n_events']}/"
                     f"{c['n_events_total']} fton={c['frac_time_on']:.2f} "
                     f"allret={c['all_returned']}")
        print(line, flush=True)

    if bar is not None:
        print(f"[controls] bar event_on_frac={bar:.4f}", flush=True)
    show("sigma=0", r_sig0)
    show("sigma=0+KICK", r_kick)
    for f in fracs:
        r = simulate_field(p, fresh(p, net, 1), f * nu_sig, do_kick=False, noise_seed=3)
        show(f"nokick frac={f:.2f}", r)
    print("\n[controls] read: (1) sigma=0+KICK must be discrete_events (wiring ok, event clears bar);"
          " (2) sigma=0 must be extinction_only; (3) does the ladder show a discrete band, only"
          " sustained, or only extinction? does coh saturate (max~1.0 = need bigger L)?", flush=True)


def run_grid(L, density, T, drives, sigma_fracs, seeds, out_path,
             sig_ref_frac=0.01, n_ref_seeds=3):
    """Multi-seed (drive x sigma_frac) grid. Verdict per cell: accepted_cell(>=60% seeds
    discrete, rate in [0.01,1]/s). Saves JSON. Each drive builds ONE network (same
    topology seed=1) and resets rng per simulation seed so runs are reproducible."""
    results = []
    for drive in drives:
        base = dict(g=3.6, L=L, density=density, T=T, seed=1)
        p = Params(nu_ext_ratio=drive, **base)
        nu_theta = compute_nu_theta(p)[0]
        nu_sig = drive * nu_theta
        print(f"\n[grid] drive={drive}  nu_sig={nu_sig*1e3:.0f}Hz  building...", flush=True)
        net = build_network(p, verbose=False)

        # per-drive calibration references (contract sef_hfo_events §10.2):
        # floor = worst-case over sigma=0 AND a sub-threshold-noise sigma_ref run
        # (must be UNIFORMLY sub-threshold across drives or the floor is event-contaminated;
        #  sig_ref_frac small, multi-seed worst-case). peak = sigma=0 deterministic kick.
        r_floor0 = simulate_field(p, fresh(p, net, 1), 0.0, do_kick=False)
        ref_series = [r_floor0["ext_coh"]]
        for rs in range(1, n_ref_seeds + 1):
            r_ref = simulate_field(p, fresh(p, net, rs), sig_ref_frac * nu_sig,
                                   do_kick=False, noise_seed=1000 + rs)
            ref_series.append(r_ref["ext_coh"])
        r_kick  = simulate_field(p, fresh(p, net, 1), 0.0, do_kick=True)
        floor_wc = max(float(np.max(s)) for s in ref_series)
        try:
            cal = calibrate_detector(ref_series, r_kick["ext_coh"])
            bar = cal["event_on_frac"]
        except UndetectableOperatingPoint:
            bar = None
        print(f"[grid] drive={drive} floor(sig0+sig_ref worst-case)={floor_wc:.4f} "
              f"kick_peak={r_kick['ext_coh'].max():.4f} bar={bar}", flush=True)

        for frac in sigma_fracs:
            sigma_n = frac * nu_sig
            cell_labels = []
            cell_rates = []
            for seed in seeds:
                r = simulate_field(p, fresh(p, net, seed), sigma_n, do_kick=False,
                                   noise_seed=seed * 17)
                e = r["ext_coh"]
                if bar is not None:
                    c = classify_run(e, r["dt_bin_ms"], op={}, window="A",
                                     event_on_frac=bar)
                    is_disc = c["label"] == "discrete_events"
                    ev_rate = c["n_events"] / (T * 1e-3) if is_disc else 0.0
                    cell_labels.append(c["label"])
                    cell_rates.append(ev_rate if is_disc else None)
                    print(f"  drive={drive} frac={frac:.3f} seed={seed} "
                          f"-> {c['label']}/{c['reason']} n_ev={c['n_events']} "
                          f"fton={c['frac_time_on']:.2f} rate={ev_rate:.3f}/s", flush=True)
                else:
                    cell_labels.append("undetectable")
                    cell_rates.append(None)

            n_disc = sum(1 for l in cell_labels if l == "discrete_events")
            frac_disc = n_disc / len(seeds)
            disc_rates = [r for r in cell_rates if r is not None and r > 0]
            mean_rate = float(np.mean(disc_rates)) if disc_rates else None
            acc = accepted_cell(frac_disc, mean_rate)
            results.append(dict(drive=drive, sigma_frac=frac, sigma_n=sigma_n,
                                n_seeds=len(seeds), n_discrete=n_disc,
                                frac_discrete=frac_disc, mean_rate=mean_rate,
                                accepted=acc, labels=cell_labels))
            print(f"  CELL drive={drive} frac={frac:.3f}: {n_disc}/{len(seeds)} disc "
                  f"frac={frac_disc:.2f} mean_rate={mean_rate} accepted={acc}", flush=True)

    summary = dict(drives=drives, sigma_fracs=sigma_fracs, seeds=seeds,
                   L=L, density=density, T=T, cells=results,
                   n_accepted=sum(1 for r in results if r["accepted"]),
                   robust_2d=sum(1 for r in results if r["accepted"]) >= 2)
    with open(out_path, "w") as f:
        json.dump(summary, f, indent=2, default=float)
    print(f"\n[grid] DONE  n_accepted={summary['n_accepted']}  "
          f"robust_2d={summary['robust_2d']}  -> {out_path}", flush=True)
    return summary


def run_tausweep(L, density, T, drive, taus, fracs, seeds, out_path):
    """Advisor blocker (2026-06-05): the robust_2d=False verdict rests entirely on the
    rate gate (events too frequent). Event rate may be set by the noise correlation time
    TAU_NOISE, not by an intrinsic network limit. Sweep tau at the fracs that failed ONLY
    on rate; if slower noise drops the rate into [0.01,1]/s the verdict flips."""
    base = dict(g=3.6, L=L, density=density, T=T, seed=1)
    p = Params(nu_ext_ratio=drive, **base)
    nu_theta = compute_nu_theta(p)[0]
    nu_sig = drive * nu_theta
    print(f"[tausweep] drive={drive} nu_sig={nu_sig*1e3:.0f}Hz taus={taus} fracs={fracs} "
          f"seeds={seeds}  building...", flush=True)
    net = build_network(p, verbose=False)
    r_floor = simulate_field(p, fresh(p, net, 1), 0.0, do_kick=False)
    r_kick  = simulate_field(p, fresh(p, net, 1), 0.0, do_kick=True)
    bar = calibrate_detector([r_floor["ext_coh"]], r_kick["ext_coh"])["event_on_frac"]
    print(f"[tausweep] bar={bar:.4f}", flush=True)

    rows = []
    for tau in taus:
        for frac in fracs:
            rates, labels = [], []
            for seed in seeds:
                r = simulate_field(p, fresh(p, net, seed), frac * nu_sig, do_kick=False,
                                   noise_seed=seed * 17, tau_noise=tau)
                c = classify_run(r["ext_coh"], r["dt_bin_ms"], op={}, window="A",
                                 event_on_frac=bar)
                is_disc = c["label"] == "discrete_events"
                ev_rate = c["n_events"] / (T * 1e-3) if is_disc else 0.0
                rates.append(ev_rate); labels.append(c["label"])
                print(f"  tau={tau:>4.0f} frac={frac:.3f} seed={seed} -> {c['label']} "
                      f"n_ev={c['n_events']} rate={ev_rate:.2f}/s fton={c['frac_time_on']:.2f}",
                      flush=True)
            n_disc = sum(1 for l in labels if l == "discrete_events")
            disc_rates = [x for x in rates if x > 0]
            mean_rate = float(np.mean(disc_rates)) if disc_rates else None
            acc = accepted_cell(n_disc / len(seeds), mean_rate)
            rows.append(dict(tau=tau, frac=frac, n_discrete=n_disc, n_seeds=len(seeds),
                             mean_rate=mean_rate, accepted=acc, labels=labels))
            print(f"  ROW tau={tau:.0f} frac={frac:.3f}: {n_disc}/{len(seeds)} disc "
                  f"mean_rate={mean_rate} accepted={acc}", flush=True)
    summary = dict(drive=drive, taus=taus, fracs=fracs, seeds=seeds, L=L, density=density,
                   T=T, rows=rows, n_accepted=sum(1 for r in rows if r["accepted"]))
    with open(out_path, "w") as f:
        json.dump(summary, f, indent=2, default=float)
    print(f"\n[tausweep] DONE n_accepted={summary['n_accepted']} -> {out_path}", flush=True)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--mode", choices=["controls", "grid", "tausweep"], default="controls")
    ap.add_argument("--taus", type=str, default="150,300,600,1000")
    ap.add_argument("--L", type=float, default=3.0)
    ap.add_argument("--density", type=float, default=1800.0)
    ap.add_argument("--T", type=float, default=1500.0)
    ap.add_argument("--drive", type=float, default=0.6)
    # grid-only args
    ap.add_argument("--drives", type=str, default="0.5,0.6,0.65,0.7")
    ap.add_argument("--fracs", type=str, default="0.0,0.02,0.05,0.10,0.20,0.40")
    ap.add_argument("--seeds", type=str, default="1,2,3")
    ap.add_argument("--out", type=str, default=None)
    args = ap.parse_args()
    if args.mode == "controls":
        run_controls(args.L, args.density, args.T, args.drive)
    elif args.mode == "tausweep":
        taus  = [float(x) for x in args.taus.split(",")]
        fracs = [float(x) for x in args.fracs.split(",")]
        seeds = [int(x)   for x in args.seeds.split(",")]
        out = args.out or os.path.join(DATA, "snn_noise_tausweep.json")
        run_tausweep(args.L, args.density, args.T, args.drive, taus, fracs, seeds, out)
    else:
        drives = [float(x) for x in args.drives.split(",")]
        fracs  = [float(x) for x in args.fracs.split(",")]
        seeds  = [int(x)   for x in args.seeds.split(",")]
        out = args.out or os.path.join(DATA, "snn_noise_grid.json")
        run_grid(args.L, args.density, args.T, drives, fracs, seeds, out)


if __name__ == "__main__":
    main()
